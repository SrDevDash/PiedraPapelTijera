    public String Resultado(char Jugador, char robo){
        String resultado= "";
        if(Jugador == 'P'){
            if(robo == 'P'){
                resultado = "Empate";
                Empates++;
            }
            else{
                if(robo == 'A'){
                    resultado = "Perdiste";
                    Perdidas++;
                    
                }
                else{
                    resultado = "Ganaste";
                    Ganadas++;
                }
            }
        }
        if(Jugador == 'A'){
            if(robo == 'P'){
                resultado = "Ganaste";
                Ganadas++;
            }
            else{
                if(robo == 'A'){
                    resultado = "Empate";
                    Empates++;
                }
                else{
                    resultado = "Perdiste";
                    Perdidas++;
                }
            }
        }
        if(Jugador == 'T'){
            if(robo == 'P'){
                resultado = "Perdiste";
                Perdidas++;
            }
            else{
                if(robo == 'A'){
                    resultado = "Ganaste";
                    Ganadas++;
                }
                else{
                    resultado = "Empate";
                    Empates++;
                }
            }
        }
        SonidoDay(resultado);
        return resultado;
        
    
    }
    public char Robo(){
        JugadaRobo.setFont(font);
        JugadaRobo.setForeground(Color.yellow);
        int num = r.nextInt(3)+1;
        if(num == 1){
            JugadaRobo.setText("Piedra");
            return 'P';       
        }
        else{
            if(num ==2){
                JugadaRobo.setText("Papel");
                return 'A';
            }
            else{
                JugadaRobo.setText("Tijera");
                return 'T';
            }
        }
    }
    
